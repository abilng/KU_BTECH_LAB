ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	S1 DB 10 DUP(?)
	MSG1 DB 0AH,0DH,'ENTER NO OF NUMBERS :$',09H
	MSG2 DB 0AH,0DH,' ENTER THE NUMBERS ::$',09H
	MSG3 DB 0AH,0DH,'PRIME NOs ARE :$',09H	
	S DB 1
	TEN DB 0AH
	TMP1 DB 1 DUP(0)
	TMP2 DB 1 DUP(0)
DATA ENDS


READ MACRO A

	MOV AH,01H
	INT 21H
	SUB AL,30H

	MUL TEN
	MOV A,AL

	MOV AH,01H
	INT 21H
	SUB AL,30H

	ADD A,AL

ENDM

PRINT MACRO G
	XOR AX,AX
	MOV TMP1,00H
	MOV TMP2,00H

	MOV AL,G
	DIV TEN
	MOV TMP1,AL
	MOV TMP2,AH

	MOV DL,TMP1
	ADD DL,30H
	MOV AH,02H
	INT 21H

	MOV DL,TMP2
	ADD DL,30H
	MOV AH,02H
	INT 21H

ENDM


CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX

	MOV AH,09H
	LEA DX,MSG1
	INT 21H

	READ S

	MOV AH,09H
	LEA DX,MSG2
	INT 21H

	PRINT S

	MOV  AH, 4CH
	INT 21H

CODE ENDS
END START
