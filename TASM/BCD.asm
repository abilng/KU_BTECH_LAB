DATA SEGMENT

N1 DB 8 DUP(0)
N2 DB 8 DUP(0)
N3 DB 8 DUP(0)
N4 DB 8 DUP(0)
CARRY DB 8 DUP(0)
BORROW DB 8 DUP(0)

MSG1 DB 0AH,0DH,'ENTER THE 1ST NO :$'
MSG2 DB 0AH,0DH,'ENTER THE 2ND NO :$'
MSG3 DB 10,13,'SUM :$'
MSG4 DB 10,13,'DIFF :$'
TEN DB 10
DATA ENDS

CODE SEGMENT
ASSUME DS:DATA,CS:CODE

START:

	MOV AX,DATA
	MOV DS,AX

	MOV AH,09H
	LEA DX,MSG1
	INT 21

	MOV CX,08H
	MOV DI,00H
	MOV SI,00H

L1:	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV N1[SI],AL
	INC SI
	LOOP L1


	MOV AH,09H
	LEA DX,MSG2
	INT 21H

	MOV CX,08H
	
	MOV SI,00H

L2:	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV N2[SI],AL
	INC SI
	LOOP L2

	MOV AH,09H
	LEA DX,MSG3
	INT 21H

	MOV CX,08H
	MOV SI,07H
	MOV DI,08H

L3:	MOV AL,N1[SI]
	ADD AL,N2[SI]
	ADD AL,CARRY

	XOR AH,AH
	DIV TEN
	MOV CARRY,AL
	MOV N3[DI],AH
	DEC SI
	DEC DI
	LOOP L3

	MOV AL,CARRY
	MOV N3,AL
	MOV CX,09H
	MOV SI,00H


L4:	MOV DL,N3[SI]
	ADD DL,30H
	MOV AH,02H
	INT 21H
	INC SI
	LOOP L4

	MOV AH,09H
	LEA DX,MSG4
	INT 21H

	MOV CX,08H
	MOV SI,07H
	MOV DI,07H

L6:	

	MOV AL,N1[SI]
	SUB AL,N2[SI]
	SUB AL,BORROW


	MOV BL,N1[SI]
        SUB BL,BORROW
	CMP BL,N2[SI]

	JC L7	

	MOV N4[DI],AL
	MOV BORROW,00H
	DEC SI
	DEC DI

	LOOP L6


	MOV CX,08H
	MOV SI,00H

L8:	MOV DL,N4[SI]
	ADD DL,30H
	MOV AH,02H
	INT 21H
	INC SI
	LOOP L8
	MOV AH,4CH
	INT 21H


L7:	MOV AL,N1[SI]
	ADD AL,0AH
	SUB AL,N2[SI]
	SUB AL,BORROW
	MOV BORROW,01H
	MOV N4[DI],AL
	DEC SI
	DEC DI
	DEC CX
	JNZ L6
	CODE ENDS
	END START





