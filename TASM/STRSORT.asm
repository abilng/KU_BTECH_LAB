DATA SEGMENT
	MSG1 DB 0AH,0DH,'ENTER THE STRING :$',09H
	MSG2 DB  0AH,0DH,'SORTED STRING IS :$',09H
	NAMEPAR LABEL BYTE
	MAXLEN DB 20
	ACTLEN DB ?
	NAMEFLD DB 20 DUP(' ')
DATA ENDS
	
PRINT MACRO A
	LEA DX,A
	MOV AH,09H
	INT 21H
ENDM

READ MACRO A
	LEA DX,A
	MOV AH,0AH
	INT 21H
ENDM
	
CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START:
	MOV AX,DATA
	MOV DS,AX
	PRINT MSG1
	READ NAMEPAR
	
	MOV SI,OFFSET NAMEFLD
	MOV DI,OFFSET NAMEFLD
	XOR CX,CX
	MOV CL,ACTLEN
	ADD CX,SI
	MOV DX,CX
	DEC DX
L1:
	MOV AL,[SI]
	MOV DI,SI
	INC DI
L2:
	MOV BL,[DI]
	CMP AL,BL
	JL L3
	XCHG AL,BL
	MOV [SI],AL
	MOV [DI],BL
L3:
	INC DI
	CMP DI,CX
	JL L2
	INC SI
	CMP SI,DX
	JL L1
	
	PRINT MSG2
;PRINT THE SORTED STRING

	MOV SI,OFFSET NAMEFLD
	MOV DI,SI
	XOR CX,CX
	MOV CL,ACTLEN
	ADD DI,CX
	INC DI
	MOV AL,'$'
	MOV [DI],AL
	PRINT NAMEFLD
	
	MOV  AH, 4CH
	INT 21H

CODE ENDS
END START
	  
