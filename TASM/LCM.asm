DATA SEGMENT
	MSG1 DB 0AH,0DH,'ENTER THE 1-ST NO :$',09H
	MSG2 DB 0AH,0DH,'ENTER THE 2-ND NO :$',09H
	MSG3 DB 0AH,0DH,'LCM IS :$',09H
	A DB 1
	B DB 1
	G DB 1
	LCM  DB 1
	LCM1 DB 1
	LCM2 DB 1
	TEN DB 0AH
DATA ENDS
	
CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV  AH, 09H
	LEA  DX,MSG1
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	
	MUL TEN
	MOV A,AL
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	
	ADD A,AL
	
	
	MOV  AH, 09H
	LEA  DX,MSG2
	INT 21H
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MUL TEN
	MOV B,AL
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	ADD B,AL
	
	MOV AL,A
	MOV BL,B
L:
	CMP AL,BL
	JE GCD
	JL L1
	SUB AL,BL
	JMP L

L1:
	SUB BL,AL	
	JMP L
	
GCD:
	MOV G,AL
	MOV AL,A
	MUL B
	DIV G
	MOV LCM,AL
	
	XOR AH,AH
	
	DIV TEN
	MOV LCM1,AL
	MOV LCM2,AH
	
	MOV  AH, 09H
	LEA  DX,MSG3
	INT 21H
	

	MOV DL,LCM1
	ADD DL,30H
	MOV AH,02H
	INT 21H
	
	MOV DL,LCM2
	ADD DL,30H
	MOV AH,02H
	INT 21H
	
	MOV  AH, 4CH
	INT 21H
CODE ENDS
END START
