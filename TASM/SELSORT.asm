DATA SEGMENT
	MSG1 DB 0AH,0DH,'ENTER NO OF NUMBERS :$',09H
	MSG2 DB 0AH,0DH,' ENTER THE NUMBER ::$',09H
	MSG3 DB 0AH,0DH,'SORTED NOs ARE :$',09H
	NO DB 1
	TEN DB 0AH
	TAB DB 09H
	TMP1 DB 1
	TMP2 DB 1
	T DB 1
	ARRAY DB 20  DUP(?)
DATA ENDS
	
READ MACRO A
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	
	MUL TEN
	MOV A,AL
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	
	ADD A,AL
	
ENDM
	
PRINT MACRO G
	XOR AX,AX
	MOV AL,G
	DIV TEN
	MOV TMP1,AL
	MOV TMP2,AH
	
	MOV DL,TMP1
	ADD DL,30H
	MOV AH,02H
	INT 21H
	
	MOV DL,TMP2
	ADD DL,30H
	MOV AH,02H
	INT 21H
	
ENDM
	
	
CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV  AH, 09H
	LEA  DX,MSG1
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NO,AL
	
	
	XOR CX,CX
	MOV CL,NO
	MOV SI,OFFSET ARRAY
	
	MOV  AH, 09H
	LEA  DX,MSG2
	INT 21H
L:
	MOV  AH, 02H
	MOV  DL,TAB
	INT 21H
	
	READ [SI]
	INC SI
	LOOP L
	MOV BX,SI
	
	MOV  AH, 09H
	LEA  DX,MSG3
	INT 21H
	
	
;SORT
	
	XOR CX,CX
	MOV CL,NO
	MOV SI,OFFSET ARRAY
	
L2:
	MOV DI,SI
	MOV BP,DI
	MOV AL,[DI]
L3:
	CMP AL,[DI]
	JLE L4
	MOV BP,DI
	MOV AL,[BP]
L4:
	INC DI
	CMP DI,BX
	JNE L3
	MOV AL,[BP]
	MOV DL,[SI]
	MOV [SI],AL
	MOV [BP],DL
	INC SI
	LOOP L2
	
	
	
;PRINT
	XOR CX,CX
	MOV CL,NO
	MOV SI,OFFSET ARRAY
L1:
	PRINT [SI]
	MOV  AH, 02H
	MOV  DL,TAB
	INT 21H
	INC SI
	LOOP L1
	
	
	MOV  AH, 4CH
	INT 21H

CODE ENDS
END START
